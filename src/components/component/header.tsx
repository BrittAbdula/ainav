import Link from "next/link"
import { Button } from "@/components/ui/button"
import { AvatarImage, AvatarFallback, Avatar } from "@/components/ui/avatar"
import { auth, signIn, signOut } from "@/auth"
import {
    Dialog,
    DialogContent,
    DialogFooter,
    DialogHeader,
    DialogTitle,
    DialogTrigger,
    DialogClose,
} from "@/components/ui/dialog"


export default async function Header() {
    const session = await auth();
    const avatar = session?.user?.image;
    return (
        <header className=" top-0 flex h-16 items-center gap-4 border-b bg-background px-4 md:px-6">
            <Link className="flex items-center gap-2 text-lg font-semibold md:text-base" href="#">
            <img alt="Aiwebsiteslist Logo" loading="lazy" width="30" height="30" decoding="async" data-nimg="1" src="/logo.svg" />
                <span className="sr-only">AIwebsitesList</span>
            </Link>
            <Link className="text-foreground transition-colors hover:text-foreground" href="#">
            AIwebsitesList
            </Link>

            <div  className="flex items-center gap-2">

                <Dialog>
                    <DialogTrigger>
                        {avatar ?
                            <Avatar>
                                <AvatarImage src={avatar} />
                                <AvatarFallback>CN</AvatarFallback>
                            </Avatar>
                            :
                            <CircleUserIcon className="h-5 w-5" />
                        }
                        <span className="sr-only">Toggle user Login</span>
                    </DialogTrigger>
                    <DialogContent>
                        <DialogHeader>
                            <DialogTitle>Are you absolutely sure?</DialogTitle>
                        </DialogHeader>
                            <DialogFooter>
                            <DialogClose  className="w-full" asChild>
                                {avatar ?
                                    <form action={async () => {
                                        "use server"
                                        await signOut()
                                    }}>
                                        <Button variant="outline" className="w-full">Sign out</Button>
                                    </form>
                                    :
                                    <form action={async () => {
                                        "use server"
                                        await signIn()
                                    }}>
                                        <Button variant="outline" className="w-full">Sign In</Button>
                                    </form>
                                }
                                </DialogClose>
                            </DialogFooter>
                    </DialogContent>
                </Dialog>
            </div>
            
        </header>
    )
}

